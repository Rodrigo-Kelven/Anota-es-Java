package variaveis;

public class Conversao {
	public static void main(String[] args) {
		
		double salario = 2700.50;
		int valor = (int) salario;
		
		System.out.printf("Salario int: %d\n", valor);
		System.out.printf("Salario double: %.2f\n", salario);
		
		long numeroGrande = 1326547894855521L;
		
		System.out.println(numeroGrande);
		
		
	}
}
